def get_maze(base_maze, visited=None, frontier=None, path=None, start=None, goal=None):
    # Copia la grilla base para no modificar el original
    grid = np.array(base_maze)
    # Marca nodos visitados
    if visited:
        for pos in visited:
            x, y = pos
            if grid[x, y] == 0:
                grid[x, y] = 3
    # Marca frontera
    if frontier:
        for pos in frontier:
            x, y = pos
            if grid[x, y] == 0:
                grid[x, y] = 6
    # Marca camino soluciÃ³n
    if path:
        for pos in path:
            x, y = pos
            if grid[x, y] == 0 or grid[x, y] == 3 or grid[x, y] == 6:
                grid[x, y] = 2
    # Marca inicio y meta
    if start:
        x, y = start
        grid[x, y] = 4
    if goal:
        x, y = goal
        grid[x, y] = 5
    return grid